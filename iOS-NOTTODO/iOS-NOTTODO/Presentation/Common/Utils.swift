import UIKit

public final class Utils {
   class func setUpUITabBar() {
        let appearance = UITabBar.appearance()
        appearance.backgroundImage = UIImage()
        appearance.shadowImage = UIImage()
        appearance.clipsToBounds = true
    }
}
